let currentLang = localStorage.getItem('language') || 'en';

function setLanguage(lang) {
  currentLang = lang;
  localStorage.setItem('language', lang);
  
  document.querySelectorAll('[data-i18n]').forEach(function(el) {
    const key = el.getAttribute('data-i18n');
    if (translations[lang] && translations[lang][key]) {
      el.innerHTML = translations[lang][key];
    }
  });
  
  document.querySelectorAll('.lang-btn').forEach(function(btn) {
    btn.classList.remove('active');
    if (btn.getAttribute('data-lang') === lang) {
      btn.classList.add('active');
    }
  });
  
  var flags = { en: '🇺🇸', es: '🇪🇸', pt: '🇧🇷' };
  document.getElementById('current-lang').innerHTML = '<span class="current-lang-flag">' + flags[lang] + '</span> <span class="current-lang-text">' + lang.toUpperCase() + '</span>';
}

document.addEventListener('DOMContentLoaded', function() {
  setLanguage(currentLang);
  
  document.querySelectorAll('[data-cal-trigger]').forEach(function(btn) {
    btn.addEventListener('click', function(e) {
      if (typeof Cal !== 'undefined') {
        e.preventDefault();
        Cal.ns['m1-labs']('ui');
      }
    });
  });
});
